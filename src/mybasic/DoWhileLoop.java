package mybasic;

public class DoWhileLoop {

    public static void main(String[] args) {

        int i=20;

        do {
            System.out.println("result="+i);// do print the variable value 1st, then they check the condition.
            i++; //i++ (mean loops run) work when the condition will match.
        }
        while(i!=30);
    }




}
